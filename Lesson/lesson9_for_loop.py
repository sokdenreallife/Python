# 1. For Loop in Python (print on separate letters in sentences)
for variable_name in "sokden":
    print(variable_name)

# 2. For Loop in list
my_list = ["Sok", "Den", "Dang"]
for variable_name_2 in my_list:
    print(variable_name_2)

# 3. For Loop
friends = ["Sok", "Den", "Dang"]
for index in range(len(friends)):
    print(friends[index])

# 4. For Loop in range (not including 10)
for variable_name_3 in range(10): # or range(3, 10)
    print(variable_name_3)

# 5. For Loop
for index_2 in range(5):
    if index_2 == 0:
        print("First Interation")
    else:
        print("Not First")